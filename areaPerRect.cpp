// This program finds the area and perimeter of a rectangle in C++


#include <cmath>
#include <iostream>

int main() {
    std::cout << "The dimensions of a rectangle are:" << std::endl;
    std::cout << "6 cm x 2 cm" << std::endl;
    std::cout << "The area is " << (6 * 2) << " cm^2" << std::endl;
    std::cout << "The perimeter is " << (2 * (6 + 2)) << " cm" << std::endl;
}
